@pytest.mark.parametrize("key", list(_GOLDEN.keys()))
def test_golden_value(client: TestClient, key: str):
    params = _PARAMS[key]
    resp = client.get("/v1/greeks", params=params, headers={"Accept": "application/json"})
    assert resp.status_code == 200
    outputs = resp.json()["greeks"]["outputs"]
    expected = _GOLDEN[key]
    for greek, expected_val in expected.items():
        assert abs(outputs[greek] - expected_val) < 1e-5, f"{key} {greek}: {outputs[greek]} != {expected_val}"
